import numpy as np

def sentence_vector(sentence, model):

    words = sentence.lower().split()

    vecs = []

    for w in words:
        if w in model.wv:
            vecs.append(model.wv[w])

    return np.mean(vecs, axis=0)


def cosine_similarity(v1,v2):

    return np.dot(v1,v2) / (np.linalg.norm(v1)*np.linalg.norm(v2))


