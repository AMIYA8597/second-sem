#Build counts
transition_counts = defaultdict(lambda: defaultdict(int))
emission_counts = defaultdict(lambda: defaultdict(int))
tag_counts = defaultdict(int)

# sentences format: [[(word, tag), (word, tag)], ...]
for sent in sentences:
    prev_tag = "<START>"

    for word, tag in sent:
        transition_counts[prev_tag][tag] += 1
        emission_counts[tag][word.lower()] += 1
        tag_counts[tag] += 1
        prev_tag = tag

    transition_counts[prev_tag]["<END>"] += 1


# Convert counts to probabilities
transition_probs = defaultdict(dict)
emission_probs = defaultdict(dict)

for prev_tag in transition_counts:
    total = sum(transition_counts[prev_tag].values())
    for tag in transition_counts[prev_tag]:
        transition_probs[prev_tag][tag] = transition_counts[prev_tag][tag] / total


for tag in emission_counts:
    total = tag_counts[tag]
    for word in emission_counts[tag]:
        emission_probs[tag][word] = emission_counts[tag][word] / total

# Viterbi Algorithm
def viterbi(sentence, tags, transition_probs, emission_probs):
    V = [{}]
    path = {}

    for tag in tags:
        V[0][tag] = (
            transition_probs["<START>"].get(tag, 1e-6) *
            emission_probs[tag].get(sentence[0], 1e-6)
        )
        path[tag] = [tag]

    for t in range(1, len(sentence)):
        V.append({})
        new_path = {}

        for curr_tag in tags:
            (prob, state) = max(
                (
                    V[t-1][prev_tag] *
                    transition_probs[prev_tag].get(curr_tag, 1e-6) *
                    emission_probs[curr_tag].get(sentence[t], 1e-6),
                    prev_tag
                )
                for prev_tag in tags
            )

            V[t][curr_tag] = prob
            new_path[curr_tag] = path[state] + [curr_tag]

        path = new_path

    (prob, state) = max((V[len(sentence)-1][tag], tag) for tag in tags)
    return path[state]


