import numpy as np

# Activation functions
def sigmoid(x):
    return 1 / (1 + np.exp(-x))

def tanh(x):
    return np.tanh(x)

# GRU Cell
def GRU_Cell(x_t, h_tminus_1, W_z, U_z, b_z,W_r, U_r, b_r,W_h, U_h, b_h):

    # Update gate
    z_t = sigmoid(np.dot(W_z, x_t) + np.dot(U_z, h_tminus_1) + b_z)

    # Reset gate
    r_t = sigmoid(np.dot(W_r, x_t) + np.dot(U_r, h_tminus_1) + b_r)

    # Candidate hidden state
    h_tilde = tanh(np.dot(W_h, x_t) + np.dot(U_h, r_t * h_tminus_1) + b_h)

    # Final hidden state
    h_t = (1 - z_t) * h_tminus_1 + z_t * h_tilde

    return h_t

# Dimensions
input_size = 4
hidden_size = 3

# Random input and hidden state
x_t = np.random.randn(input_size, 1)
h_prev = np.random.randn(hidden_size, 1)

# Initialize weights
W_z = np.random.randn(hidden_size, input_size)
U_z = np.random.randn(hidden_size, hidden_size)
b_z = np.random.randn(hidden_size, 1)

W_r = np.random.randn(hidden_size, input_size)
U_r = np.random.randn(hidden_size, hidden_size)
b_r = np.random.randn(hidden_size, 1)

W_h = np.random.randn(hidden_size, input_size)
U_h = np.random.randn(hidden_size, hidden_size)
b_h = np.random.randn(hidden_size, 1)

print("Input Shape:", x_t.shape)
