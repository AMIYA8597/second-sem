print("U_z Shape:", U_z.shape,"\n weight:",U_z)
print("b_z Shape:", b_z.shape,"\n weight:",b_z)

print("Reset Gate")
print("W_r Shape:", W_r.shape,"\n weight:",W_r)
print("U_r Shape:", U_r.shape,"\n weight:",U_r)
print("b_r Shape:", b_r.shape,"\n weight:",b_r)

print("Hidden Gate")
print("W_h shape:",W_h.shape,"\n weight:",W_h)
